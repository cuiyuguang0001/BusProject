package com.bus.pojo;

public class Bus_job{

	//职位排序
	private int list;
	//主键
	private int id;
	//职位名
	private String name;

	public int getList(){
		return list;
	}

	public void setList(int list){
		this.list=list;
	}

	public int getId(){
		return id;
	}

	public void setId(int id){
		this.id=id;
	}

	public String getName(){
		return name;
	}

	public void setName(String name){
		this.name=name;
	}

}